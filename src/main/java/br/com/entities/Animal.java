package br.com.entities;

public class Animal {

	private String gato;
	private String cachorro;
	private String cavalo;
	private String urso;
	private String bode;
	private String cabra;
	private String zebra;

	public String getZebra() {
		return zebra;
	}

	public void setZebra(String zebra) {
		this.zebra = zebra;
	}

	public String getGato() {
		return gato;
	}

	public void setGato(String gato) {
		this.gato = gato;
	}

	public String getCachorro() {
		return cachorro;
	}

	public void setCachorro(String cachorro) {
		this.cachorro = cachorro;
	}

	public String getCavalo() {
		return cavalo;
	}

	public void setCavalo(String cavalo) {
		this.cavalo = cavalo;
	}

	public String getUrso() {
		return urso;
	}

	public void setUrso(String urso) {
		this.urso = urso;
	}

	public String getBode() {
		return bode;
	}

	public void setBode(String bode) {
		this.bode = bode;
	}

	public String getCabra() {
		return cabra;
	}

	public void setCabra(String cabra) {
		this.cabra = cabra;
	}

}
